void	*ft_memmove(void *dest, void *src, size_t n)
{
	size_t	i;
	void	*tmp;

	i = 0;
	while (dest[i] != (void *)0 && src[i] != (void *)0 && i < n)
	{
		tmp = src[i];
		dest[i] = tmp;
		i++;
	}
	return (dest);
}
